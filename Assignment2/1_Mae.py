
# Print exercises: print first name, one letter at a time
print('M')
print('A')
print('E')

# Variable exercises: Each letter of the name as separate variable
# First name's variables, letterX same as letter1:
letter1 = ('M')
letter2 = ('A')
letter3 = ('E')
letterX = ('M')
# Print letterX and letter1
print(letter1)
print(letterX)

# First name's variables, letterX is given a new letter:
letter1 = ('M')
letter2 = ('A')
letter3 = ('E')
letterX = ('X')
# Print letterX and letter1
print(letter1)
print(letterX)

# First name's variables, letterX=letter1:
letter1 = ('M')
letter2 = ('A')
letter3 = ('E')
letterX = letter1
# Print letterX and letter1
print(letter1)
print(letterX)
        
# First name's variables, letterX=letter1, letter1 is 'z':
letter1 = ('z')
letter2 = ('A')
letter3 = ('E')
letterX = letter1
# Print letterX and letter1
print(letter1)
print(letterX)